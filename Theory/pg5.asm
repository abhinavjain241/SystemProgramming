TITLE PG5
.MODEL SMALL
.STACK 100H
.DATA

PROMPT DB 'TYPE A LINE', 0DH, 0AH, '$'
NOCAP DB 0DH, 0AH, 'NO CAPITAL $'
CAP DB 0DH, 0AH, 'FIRST CAPITALS='

FIRST DB ']'
      DB ' LAST CAPITAL='
LAST  DB '@$'

 .CODE 
 MAIN PROC
 	MOV AX, @DATA
 	MOV DS, AX
 	MOV AH, 9
 	LEA DX, PROMPT
 	INT 33
 	MOV AH, 1
 	INT 33

W1:
	CMP AL, 0DH
	JE E1
	CMP AL, 'A'
	JNGE E2
	CMP AL, 'Z'
	JNLE E2
	CMP AL, FIRST
	JNL C1
	MOV FIRST, AL

C1:
	CMP AL, LAST
	JNG E2
	MOV LAST, AL

E2: 
	INT 33
	JMP W1

E1:
	MOV AH, 9
	CMP FIRST, ']'
	JNE CAPS
	LEA DX, NOCAP
	JMP D1

CAPS:
	LEA DX, CAP

D1: 
	INT 33
	
	MOV AH, 76
	INT 33
MAIN ENDP
END

